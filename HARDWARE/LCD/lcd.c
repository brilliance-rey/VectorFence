//向LCD数据总线写数据
void WriteLcdDataBus(unsigned char data)
{
	LCD_DB0 = (data >> 0) & 0x01;
	LCD_DB1 = (data >> 1) & 0x01;
	LCD_DB2 = (data >> 2) & 0x01;
	LCD_DB3 = (data >> 3) & 0x01;
	LCD_DB4 = (data >> 4) & 0x01;
	LCD_DB5 = (data >> 5) & 0x01;
	LCD_DB6 = (data >> 6) & 0x01;
	LCD_DB7 = (data >> 7) & 0x01;
}

//从LCD数据总线读数据
unsigned char ReadLcdDataBus()
{
	unsigned char rd_data = 0;
	unsigned char temp;
	
	temp = LCD_DB7;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB6;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB5;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB4;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB3;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB2;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB1;
	rd_data |= temp & 0x01;
	rd_data <<= 1;
	temp = LCD_DB0;
	rd_data |= temp & 0x01;
	
	return rd_data;	
}
/****************读“忙”函数**************************/
/*******数据口的最高位DB7==1，则busy*****************/
void LcdCheckBusy(void)//每次读写操作前都要进行busy判断
{
	LCD_DB7 = 0;//并行总线bit7为busy信号，当DB7==1时，则为busy
	LCD_DI = 0;
	LCD_RW = 1;
	LCD_EN = 1;
	delay_us(RW_DELAY_US);
	while(LCD_DB7);//等待busy为0
	LCD_EN = 0;
	delay_us(RW_DELAY_US);
}
/****************LCD写命令函数********************/
void LcdWriteCmd(unsigned char cmd)
{
	LcdCheckBusy();
	LCD_DI = 0;
	LCD_RW = 0;
	WriteLcdDataBus(cmd);
	LCD_EN = 1;
	delay_us(RW_DELAY_US);
	LCD_EN = 0;
	delay_us(RW_DELAY_US);
}
/****************LCD写数据************************/
void LcdWriteData(unsigned char data)
{
	LcdCheckBusy();
	LCD_DI = 1;
	LCD_RW = 0;
	WriteLcdDataBus(data);
	LCD_EN = 1;
	delay_us(RW_DELAY_US);
	LCD_EN = 0;
	delay_us(RW_DELAY_US);
}
/***************页设置:显示在哪一页******************/
/*************共8页，一页是8行像素点*****************/
//X Address
void LcdSetPage(unsigned char page)
{
	page = 0xb8 | page;     //页的首地址为0xb8：page或上0xb8=选择page页
	LcdWriteCmd(page);
}
/************行设置：起始在哪一行********************/
/**********每页共8行，一般从0行开始******************/
//Z Address
void LcdSetLine(unsigned char sline)
{
	sline = 0xc0 | sline;   //起始行地址为0xc0：sline或上0xc0=选择行
	LcdWriteCmd(sline);
}
/*************起始列设置+像素点选择********************/ 
/***********每半片共64列，列值不得大于63***************/
//Y Address
void LcdSetColumn(unsigned char column)
{
	column = 0x3f & column;   //与上列的最大值63:0x3f  可得所选列值<63
	column = 0x40 | column;   //得列的首地址
	LcdWriteCmd(column);
}
/*************设置显示的开关********************/ 
/**********0为关闭显示；1为 开启显示************/
void Lcd_On_Off(unsigned char set)
{
	set = 0x3e | set;        //=0011 111x----0x3e为关闭显示；0x3f为开启显示
	LcdWriteCmd(set);
}

/*************选择左右半屏显示********************/ 
/********0-全屏显示；1-左；2-右********************/ 
/************CS片选端高电平有效********************/ 
void Lcd_Cs(unsigned char cs_sel)
{
  switch(cs_sel)
  {
    case 0://全屏显示
      LCD_CS1 = 1;
      LCD_CS2 = 1;
      break;     
    case 1://左显示
      LCD_CS1 = 1;
      LCD_CS2 = 0;
      break;     
    case 2://右显示
      LCD_CS1 = 0;
      LCD_CS2 = 1;
      break;     
    default:
      break;
    }
}
/*************清屏：选择半屏********************/
/********0-清全屏；1-清左；2-清右***************/ 
void Lcd_Clear(unsigned char cs_sel)
{
	unsigned char i,j;
	Lcd_Cs(cs_sel);
	for(i=0;i<8;i++)
	{
		LcdSetPage(i);
		LcdSetColumn(0);
		for(j=0;j<64;j++)
        {
        	LcdWriteData(0x00);   //每列全部写0，列地址指针自动+1
        }
    }
} 


//LCD复位操作
void Lcd_Reset(void)
{
	LCD_RST = 0;
	delay_us(10);
	LCD_RST = 1;
	delay_us(10);
}

